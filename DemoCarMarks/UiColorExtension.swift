import UIKit

extension UIColor {
    public class var rbGreen: UIColor {
        return UIColor(red:0.75, green:0.90, blue:0.31, alpha:1.0)
    }
    public class var rbBlack: UIColor {
        return UIColor(red:0.08, green:0.08, blue:0.08, alpha:1.0)
    }
    public class var rbDarkGrey: UIColor {
        return UIColor(red:0.31, green:0.31, blue:0.31, alpha:1.0)
    }
}
